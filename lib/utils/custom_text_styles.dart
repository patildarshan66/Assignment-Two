

import 'package:flutter/material.dart';


/// constant text style declared with font size 13 and font weight 500
TextStyle st13pt500w({Color? color}){
  return TextStyle(fontWeight: FontWeight.w500,fontSize: 13,color: color);
}


/// constant text style declared with font size 13 and font weight bold
TextStyle st15ptBold({Color? color}){
  return TextStyle(fontWeight: FontWeight.bold,fontSize: 15,color: color);
}


/// constant text style declared with font size 13 and font weight bold
TextStyle st20ptBold({Color? color}){
  return TextStyle(fontWeight: FontWeight.bold,fontSize: 20,color: color);
}